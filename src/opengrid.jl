function read_opengrid_out(io::IO)
    nkpts = 0
    kgrid = nothing
    kpoints = nothing
    kweights = nothing

    for rawline in eachline(io)
        line = strip(rawline)
        if startswith(line, "EXX: q-point mesh:")
            vals = split(line)
            kgrid = parse.(Int, vals[end-2:end])
            nkpts = prod(kgrid)
            kpoints = [zeros(Float64, 3) for _ in 1:nkpts]
            kweights = zeros(Float64, nkpts)
        end
        if startswith(line, "List to be put in the .win file of wannier90:")
            for ik in 1:nkpts
                kp_line = readline(io)
                kp_line = strip(kp_line)
                vals = parse.(Float64, split(kp_line))
                kpoints[ik] = vals[1:3]
                kweights[ik] = vals[4]
            end
        end
    end
    return kgrid, kpoints, kweights
end

function read_opengrid_out(filename::AbstractString)
    return open(filename) do io
        read_opengrid_out(io)
    end
end
